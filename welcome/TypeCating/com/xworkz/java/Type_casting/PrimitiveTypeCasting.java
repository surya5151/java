package com.xworkz.java.Type_casting;

public class PrimitiveTypeCasting {
	public static void main(String[] args) {
		//implicit type casting
		int a=100;
		long l=a;
		System.out.println("L: "+l);
		
		//Explicit type casting
		double d =2000.00;
		int i= (int) d;
		System.out.println("I: "+i);
		
		 
		
	}

}
